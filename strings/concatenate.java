package strings;

public class concatenate {
    public static void main(String[] args) {
        String str1="samar";
        String str2="preet";

        String full_name=str1+str2;
        System.out.println(full_name);
        
        String str3="Singh";
        full_name=str1+str2+" "+str3;
        System.out.println(full_name);

    }
}
